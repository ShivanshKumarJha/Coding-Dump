s = 'Welcome to Geek\'s Course'
print(s)

s = "Hi, \nWelcome to Geek's Course"
print(s)

# Escape backslash itself
s1 = "A simple \ example"
s2 = "Backslash at the end \\"
s3 = "\\n"
s4 = "\\t"

print(s1)
print(s2)
print(s3)
print(s4)

