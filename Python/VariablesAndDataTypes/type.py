a = 10              #int
print(type(a))

b = 10.5            #float
print(type(b))

c = 2 + 4j          #complex
print(type(c))

a = True            #bool
print(type(a))

b = None            #NoneType
print(type(b))

str = "gfg"         #str 
print(type(str))

l = [10,20,30]      #list - mutable
print(type(l))

t = (10,20,30)      #tuple - these are immutable
print(type(t))

s = {10,20,30}      #set - all items are distinct
print(type(s))
    
d = {10:"gfg",20:"ide"}     #dict - key:value pairs
print(type(d))
